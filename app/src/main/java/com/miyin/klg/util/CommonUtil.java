    //密码正则表达式
    public static boolean isPwd(String str) {
        boolean rs = false;
        if (str.length() > 5 && str.length() < 9) {
            String regEx = "^[A-Za-z0-9_]+$";
            Pattern p = Pattern.compile(regEx);
            Matcher m = p.matcher(str);
            if (m.matches()) {
                rs = true;
            } else {
                rs = false;
            }
        } else {
            rs = false;
        }

        return rs;
    }

